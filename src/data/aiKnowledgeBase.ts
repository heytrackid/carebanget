  if (lowerQuery.includes('cambridge') || lowerQuery.includes('kognitif')) {
    return aiKnowledgeBase.cambridge.approach;
  }

  // Kak Seto
  if (lowerQuery.includes('kak seto') || lowerQuery.includes('komunikasi')) {
    if (lowerQuery.includes('disiplin')) {
      return aiKnowledgeBase.kak_seto.discipline;
    }
    return aiKnowledgeBase.kak_seto.communication;
  }

